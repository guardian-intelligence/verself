func RenderHAProxyTemplate(plan Plan) string {
	var b strings.Builder
	line(&b, "# Generated by github.com/verself/host-configuration/edgecontract. Edit topology inputs, then run aspect operator edge --action=render.")
	line(&b, "# Nomad-backed backend sections live in {{ haproxy_nomad_upstreams_config }} so deployments can refresh allocation membership without rewriting this file.")
	line(&b, "")
	renderGlobal(&b)
	renderDefaults(&b)
	renderHTTPFrontend(&b, plan)
	renderHTTPSFrontend(&b, plan)
	renderMetricsFrontend(&b, plan)
	renderFirecrackerFrontend(&b, plan)
	renderNomadArtifactsFrontend(&b, plan)
	renderRateBackends(&b)
	renderFixedBackends(&b, plan)
	renderRouteBackends(&b, plan)
	return strings.TrimRight(b.String(), "\n") + "\n"
}

func RenderPublicHostsMap(plan Plan) string {
	var rows []string
	for _, route := range plan.Routes {
		if len(route.Paths) == 0 {
			rows = append(rows, fmt.Sprintf("%s %s", route.FQDN, route.Backend))
		}
	}
	sort.Strings(rows)
	return strings.Join(rows, "\n") + "\n"
}

func RenderInitialNomadUpstreamsConfig(plan Plan) string {
	return RenderNomadUpstreamsConfig(plan, nil)
}

func RenderNomadUpstreamsConfig(plan Plan, endpoints []NomadEndpoint) string {
	var b strings.Builder
	line(&b, "# Generated by github.com/verself/host-configuration/edgecontract. Runtime copy is reconciled from Nomad's native service catalog.")
	line(&b, "")
	endpointsByKey := endpointsByUpstreamKey(plan, endpoints)
	for _, backend := range dynamicBackends(plan) {
		renderLiveDynamicBackend(&b, plan, backend, endpointsByKey[backend.Target.Upstream])
	}
	return strings.TrimRight(b.String(), "\n") + "\n"
}

func RenderNomadUpstreamsTemplate(plan Plan) string {
	var b strings.Builder
	line(&b, "# Generated by github.com/verself/host-configuration/edgecontract. Nomad renders this template from its native service catalog.")
	line(&b, "")
	servicesByKey := nomadServicesByUpstreamKey(plan)
	for _, backend := range dynamicBackends(plan) {
		renderTemplatedDynamicBackend(&b, plan, backend, servicesByKey[backend.Target.Upstream])
	}
	return strings.TrimRight(b.String(), "\n") + "\n"
}

func artifactIssues(artifacts []GeneratedArtifact) []string {
	var issues []string
	for _, artifact := range artifacts {
		got, err := os.ReadFile(artifact.Path)
		if err != nil {
			issues = append(issues, fmt.Sprintf("read %s: %v", artifact.Path, err))
			continue
		}
		if string(got) != artifact.Content {
			issues = append(issues, fmt.Sprintf("%s: %s is not generated from the edge contract; run aspect operator edge --action=render", artifact.Path, artifact.Name))
		}
	}
	return issues
}

