func renderRouteBackends(b *strings.Builder, plan Plan) {
	for _, route := range plan.Routes {
		if route.Upstream != "" {
			continue
		}
		linef(b, "backend %s", route.Backend)
		linef(b, "  guid %s", route.Backend)
		renderStaticRouteBackend(b, plan, route)
		line(b, "")
	}
}

func renderStaticRouteBackend(b *strings.Builder, plan Plan, route PublicRoute) {
	switch {
	case route.Kind == OriginOperator:
		line(b, "  http-request set-header Host %[req.hdr(host)]")
		line(b, "  http-request set-header X-Forwarded-Host %[req.hdr(host)]")
		line(b, "  http-request set-header X-Forwarded-Proto https")
		linef(b, "  server pomerium %s guid %s", endpointAddress(plan, "pomerium", "http"), StaticServerGUID(route.Backend, "pomerium"))
	case route.Component == "zitadel":
		linef(b, "  server zitadel %s proto h2 guid %s", endpointAddress(plan, "zitadel", "http"), StaticServerGUID(route.Backend, "zitadel"))
	case route.Component == "stalwart":
		renderStalwartBackend(b, plan, route)
	default:
		renderRoutePolicy(b, route)
	}
}

func renderStalwartBackend(b *strings.Builder, plan Plan, route PublicRoute) {
	line(b, "  acl stalwart_direct path -i /.well-known/jmap")
	line(b, "  acl stalwart_direct path_beg /jmap/ /auth/")
	line(b, "  acl mail_allowed path -i /.well-known/jmap")
	line(b, "  acl mail_allowed path_beg /jmap/ /auth/")
	line(b, "  http-request return status 404 unless mail_allowed")
	line(b, "  use-server stalwart if stalwart_direct")
	linef(b, "  server stalwart %s guid %s", endpointAddress(plan, "stalwart", "http"), StaticServerGUID(route.Backend, "stalwart"))
}

func renderRoutePolicy(b *strings.Builder, route PublicRoute) {
	if route.Component == "source_code_hosting_service" && route.Interface == "git_smart_http" {
		line(b, "  acl source_git method GET POST")
		line(b, "  acl source_git_path path_reg ^/[^/]+/[^/]+\\.git/(info/refs|git-upload-pack|git-receive-pack)$")
		line(b, "  http-request return status 404 unless source_git source_git_path")
		linef(b, "  http-request set-header X-Forwarded-Host %s", route.FQDN)
		line(b, "  http-request set-header X-Forwarded-Proto https")
		return
	}
	renderSecurityProfile(b, profileForRoute(route))
	if route.Kind == OriginPublicAPI {
		prefix := route.PathPrefix
		if prefix == "" {
			prefix = "/api/"
		}
		linef(b, "  http-request return status 404 unless { path_beg %s }", prefix)
	}
	if route.BodyLimit > 0 {
		renderBodyLimit(b, route.BodyLimit)
	}
}

func renderSecurityProfile(b *strings.Builder, profile SecurityProfile) {
	switch profile {
	case SecurityAPI:
		line(b, "  http-response set-header Content-Security-Policy \"default-src 'none'; frame-ancestors 'none'; base-uri 'none'; form-action 'none'\"")
		line(b, "  http-response set-header Cross-Origin-Resource-Policy same-origin")
		line(b, "  http-response set-header Permissions-Policy \"accelerometer=(), camera=(), geolocation=(), gyroscope=(), magnetometer=(), microphone=(), payment=(), usb=()\"")
		line(b, "  http-response set-header Referrer-Policy no-referrer")
		line(b, "  http-response set-header X-Content-Type-Options nosniff")
		line(b, "  http-response set-header X-Frame-Options DENY")
	case SecurityBrowser:
		line(b, "  http-response set-header Content-Security-Policy \"default-src 'self'; script-src 'self' 'unsafe-inline' 'wasm-unsafe-eval'; style-src 'self' 'unsafe-inline'; img-src 'self' data: blob: https:; font-src 'self' data:; connect-src 'self'; frame-ancestors 'none'; base-uri 'none'; form-action 'self'; object-src 'none'\"")
		line(b, "  http-response set-header Cross-Origin-Opener-Policy same-origin")
		line(b, "  http-response set-header Permissions-Policy \"accelerometer=(), camera=(), geolocation=(), gyroscope=(), magnetometer=(), microphone=(), payment=(), usb=()\"")
		line(b, "  http-response set-header Referrer-Policy strict-origin-when-cross-origin")
		line(b, "  http-response set-header X-Content-Type-Options nosniff")
		line(b, "  http-response set-header X-Frame-Options DENY")
	}
}

func renderBodyLimit(b *strings.Builder, limit int) {
	line(b, "  acl has_content_length req.hdr(content-length) -m found")
	line(b, "  acl has_transfer_encoding req.hdr(transfer-encoding) -m found")
	linef(b, "  http-request wait-for-body time 1s at-least %d if has_content_length", limit+1)
	linef(b, "  http-request wait-for-body time 1s at-least %d if has_transfer_encoding", limit+1)
	linef(b, "  http-request deny deny_status 413 if { req.body_size gt %d }", limit)
}

func renderLiveDynamicBackend(b *strings.Builder, plan Plan, backend Backend, endpoints []NomadEndpoint) {
	linef(b, "backend %s", backend.ID)
	linef(b, "  guid %s", backend.ID)
	line(b, "  balance random")
	if route, ok := routeByBackend(plan, backend.ID); ok {
		renderRoutePolicy(b, route)
	} else {
		renderSecurityProfile(b, backend.Profile)
		if backend.BodyLimit > 0 {
			renderBodyLimit(b, backend.BodyLimit)
		}
	}
	if len(endpoints) == 0 {
		line(b, "  http-request return status 503 content-type text/plain string \"service unavailable\"")
		line(b, "")
		return
	}
	protoPart := ""
	if backend.Target.Protocol == ProxyH2C {
		protoPart = " proto h2"
	}
	for i, endpoint := range endpoints {
		name := endpointServerName(endpoint, i)
		linef(b, "  server %s %s:%d%s check inter 1s fall 1 rise 1 guid %s", name, endpoint.Address, endpoint.Port, protoPart, StaticServerGUID(backend.ID, name))
	}
	line(b, "")
}

func renderTemplatedDynamicBackend(b *strings.Builder, plan Plan, backend Backend, service NomadService) {
	linef(b, "backend %s", backend.ID)
	linef(b, "  guid %s", backend.ID)
	line(b, "  balance random")
	if route, ok := routeByBackend(plan, backend.ID); ok {
		renderRoutePolicy(b, route)
	} else {
		renderSecurityProfile(b, backend.Profile)
		if backend.BodyLimit > 0 {
			renderBodyLimit(b, backend.BodyLimit)
		}
	}
	if service.ServiceName == "" {
		line(b, "  http-request return status 503 content-type text/plain string \"service unavailable\"")
		line(b, "")
		return
	}
	protoPart := ""
	if backend.Target.Protocol == ProxyH2C {
		protoPart = " proto h2"
	}
	linef(b, "[[ with nomadService %q ]]", service.ServiceName)
	line(b, "[[ range $i, $svc := . ]]")
	linef(b, "  server srv_[[ $i ]] [[ $svc.Address ]]:[[ $svc.Port ]]%s check inter 1s fall 1 rise 1 guid %s_srv_[[ $i ]]", protoPart, backend.ID)
	line(b, "[[ end ]]")
	line(b, "[[ else ]]")
	line(b, "  http-request return status 503 content-type text/plain string \"service unavailable\"")
	line(b, "[[ end ]]")
	line(b, "")
}

func dynamicBackends(plan Plan) []Backend {
	out := make([]Backend, 0, len(plan.Backends))
	for _, backend := range plan.Backends {
		if backend.Target.Upstream != "" {
			out = append(out, backend)
		}
	}
	sort.Slice(out, func(i, j int) bool {
		return out[i].ID < out[j].ID
	})
	return out
}

func nomadServicesByUpstreamKey(plan Plan) map[UpstreamKey]NomadService {
	out := make(map[UpstreamKey]NomadService, len(plan.NomadUpstreams))
	for _, service := range plan.NomadUpstreams {
		out[UpstreamKey(service.Key)] = service
	}
	return out
}

func endpointsByUpstreamKey(plan Plan, endpoints []NomadEndpoint) map[UpstreamKey][]NomadEndpoint {
	keyByService := make(map[string]UpstreamKey, len(plan.NomadUpstreams))
	for _, svc := range plan.NomadUpstreams {
		keyByService[svc.ServiceName] = UpstreamKey(svc.Key)
	}
	out := map[UpstreamKey][]NomadEndpoint{}
	for _, endpoint := range endpoints {
		key := keyByService[endpoint.ServiceName]
		if key == "" {
			continue
		}
		out[key] = append(out[key], endpoint)
	}
	for key := range out {
		sort.Slice(out[key], func(i, j int) bool {
			a, z := out[key][i], out[key][j]
			if a.ServiceName != z.ServiceName {
				return a.ServiceName < z.ServiceName
			}
			if a.Address != z.Address {
				return a.Address < z.Address
			}
			if a.Port != z.Port {
				return a.Port < z.Port
			}
			return a.AllocID < z.AllocID
		})
	}
	return out
}

func endpointServerName(endpoint NomadEndpoint, index int) string {
	raw := endpoint.AllocID
	if raw == "" {
		raw = endpoint.ServiceID
	}
	if raw == "" {
		raw = fmt.Sprintf("%s_%d", endpoint.ServiceName, endpoint.Port)
	}
	var sanitized strings.Builder
	for _, r := range raw {
		switch {
		case r >= 'a' && r <= 'z':
			sanitized.WriteRune(r)
		case r >= 'A' && r <= 'Z':
			sanitized.WriteRune(r)
		case r >= '0' && r <= '9':
			sanitized.WriteRune(r)
		default:
			sanitized.WriteByte('_')
		}
		if sanitized.Len() >= 16 {
			break
		}
	}
	if sanitized.Len() == 0 {
		sanitized.WriteString("endpoint")
	}
	return fmt.Sprintf("srv_%02d_%s", index+1, sanitized.String())
}

func routeByBackend(plan Plan, id BackendID) (PublicRoute, bool) {
	for _, route := range plan.Routes {
		if route.Backend == id {
			return route, true
		}
	}
	return PublicRoute{}, false
}

func routeHost(plan Plan, component, iface string) string {
	for _, route := range plan.Routes {
		if route.Component == component && route.Interface == iface {
			return route.FQDN
		}
	}
	return ""
}

func pathRoutes(plan Plan) []PublicRoute {
	var out []PublicRoute
	for _, route := range plan.Routes {
		if len(route.Paths) > 0 {
			out = append(out, route)
		}
	}
	sort.Slice(out, func(i, j int) bool {
		if out[i].FQDN == out[j].FQDN {
			return out[i].Backend < out[j].Backend
		}
		return out[i].FQDN < out[j].FQDN
	})
	return out
}

func endpointAddress(plan Plan, component, endpointName string) string {
	return endpoint(plan, component, endpointName).Address
}

func endpoint(plan Plan, component, endpointName string) Endpoint {
	componentEndpoints := plan.Components[component]
	return componentEndpoints.Endpoints[endpointName]
}

func line(b *strings.Builder, s string) {
	b.WriteString(s)
	b.WriteByte('\n')
}

func linef(b *strings.Builder, format string, args ...any) {
	b.WriteString(fmt.Sprintf(format, args...))
	b.WriteByte('\n')
}
