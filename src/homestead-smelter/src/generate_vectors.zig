// ---------------------------------------------------------------------------
// JSON generation
// ---------------------------------------------------------------------------

pub fn writeVectors(w: *Writer) !void {
    const hello = canonicalHello();
    const hello_bytes = hs.encodeHelloFrame(hello);

    const sample = canonicalSample();
    const sample_bytes = hs.encodeSampleFrame(sample);

    const sample_max = canonicalSampleMax();
    const sample_max_bytes = hs.encodeSampleFrame(sample_max);

    const request_bytes = hostp.encodeRequest(.{ .kind = .attach });

    const job_id = hs.parseUuid(job_id_str) catch unreachable;

    const packet_hello = hostp.Packet{
        .header = .{
            .kind = .hello,
            .host_seq = 7,
            .observed_wall_ns = 1_700_000_000_100_000_000,
            .job_id = job_id,
            .stream_generation = 3,
            .flags = hostp.packet_flag_snapshot,
        },
        .payload = hello_bytes,
    };
    const packet_hello_bytes = hostp.encodePacket(packet_hello);

    const packet_disconnect = hostp.Packet{
        .header = .{
            .kind = .disconnect,
            .host_seq = 42,
            .observed_wall_ns = 1_700_000_000_200_000_000,
            .job_id = job_id,
            .stream_generation = 3,
            .flags = 0,
        },
        .payload = hostp.encodeDisconnectPayload(.decode_failed),
    };
    const packet_disconnect_bytes = hostp.encodePacket(packet_disconnect);

    // -- file header --

    try w.writeAll(
        \\{
        \\  "schema_version": 1,
        \\  "note": "Generated by: zig build run-generate-vectors. Do not edit.",
        \\  "protocol": {
        \\    "guest_frame_size": 128,
        \\    "host_request_size": 32,
        \\    "host_packet_size": 176,
        \\    "host_payload_offset": 48,
        \\    "byte_order": "little-endian"
        \\  },
        \\  "u64_encoding": "decimal-string",
        \\  "vectors": {
        \\
    );

    // -- guest_hello --

    try w.writeAll("    \"guest_hello\": {\n");
    try w.writeAll("      \"layer\": \"guest\",\n");
    try w.writeAll("      \"type\": \"hello\",\n");
    try w.writeAll("      \"hex\": \"");
    try writeHex(w, &hello_bytes);
    try w.writeAll("\",\n");
    try w.writeAll("      \"fields\": {\n");
    try w.print("        \"seq\": {d},\n", .{hello.seq});
    try w.print("        \"flags\": {d},\n", .{hello.flags});
    try w.print("        \"mono_ns\": \"{d}\",\n", .{hello.mono_ns});
    try w.print("        \"wall_ns\": \"{d}\",\n", .{hello.wall_ns});
    try w.print("        \"boot_id\": \"{s}\",\n", .{hello_boot_id_str});
    try w.print("        \"mem_total_kb\": \"{d}\"\n", .{hello.mem_total_kb});
    try w.writeAll("      }\n");
    try w.writeAll("    },\n");

    // -- guest_sample --

    try w.writeAll("    \"guest_sample\": {\n");
    try w.writeAll("      \"layer\": \"guest\",\n");
    try w.writeAll("      \"type\": \"sample\",\n");
    try w.writeAll("      \"hex\": \"");
    try writeHex(w, &sample_bytes);
    try w.writeAll("\",\n");
    try w.writeAll("      \"fields\": {\n");
    try w.print("        \"seq\": {d},\n", .{sample.seq});
    try w.print("        \"flags\": {d},\n", .{sample.flags});
    try w.print("        \"mono_ns\": \"{d}\",\n", .{sample.mono_ns});
    try w.print("        \"wall_ns\": \"{d}\",\n", .{sample.wall_ns});
    try w.print("        \"cpu_user_ticks\": \"{d}\",\n", .{sample.cpu_user_ticks});
    try w.print("        \"cpu_system_ticks\": \"{d}\",\n", .{sample.cpu_system_ticks});
    try w.print("        \"cpu_idle_ticks\": \"{d}\",\n", .{sample.cpu_idle_ticks});
    try w.print("        \"load1_centis\": {d},\n", .{sample.load1_centis});
    try w.print("        \"load5_centis\": {d},\n", .{sample.load5_centis});
    try w.print("        \"load15_centis\": {d},\n", .{sample.load15_centis});
    try w.print("        \"procs_running\": {d},\n", .{sample.procs_running});
    try w.print("        \"procs_blocked\": {d},\n", .{sample.procs_blocked});
    try w.print("        \"mem_available_kb\": \"{d}\",\n", .{sample.mem_available_kb});
    try w.print("        \"io_read_bytes\": \"{d}\",\n", .{sample.io_read_bytes});
    try w.print("        \"io_write_bytes\": \"{d}\",\n", .{sample.io_write_bytes});
    try w.print("        \"net_rx_bytes\": \"{d}\",\n", .{sample.net_rx_bytes});
    try w.print("        \"net_tx_bytes\": \"{d}\",\n", .{sample.net_tx_bytes});
    try w.print("        \"psi_cpu_pct100\": {d},\n", .{sample.psi_cpu_pct100});
    try w.print("        \"psi_mem_pct100\": {d},\n", .{sample.psi_mem_pct100});
    try w.print("        \"psi_io_pct100\": {d}\n", .{sample.psi_io_pct100});
    try w.writeAll("      }\n");
    try w.writeAll("    },\n");

    // -- guest_sample_max --

    try w.writeAll("    \"guest_sample_max\": {\n");
    try w.writeAll("      \"layer\": \"guest\",\n");
    try w.writeAll("      \"type\": \"sample\",\n");
    try w.writeAll("      \"note\": \"All fields at maximum value. Tests u64/u32/u16 boundary handling.\",\n");
    try w.writeAll("      \"hex\": \"");
    try writeHex(w, &sample_max_bytes);
    try w.writeAll("\",\n");
    try w.writeAll("      \"fields\": {\n");
    try w.print("        \"seq\": {d},\n", .{sample_max.seq});
    try w.print("        \"flags\": {d},\n", .{sample_max.flags});
    try w.print("        \"mono_ns\": \"{d}\",\n", .{sample_max.mono_ns});
    try w.print("        \"wall_ns\": \"{d}\",\n", .{sample_max.wall_ns});
    try w.print("        \"cpu_user_ticks\": \"{d}\",\n", .{sample_max.cpu_user_ticks});
    try w.print("        \"cpu_system_ticks\": \"{d}\",\n", .{sample_max.cpu_system_ticks});
    try w.print("        \"cpu_idle_ticks\": \"{d}\",\n", .{sample_max.cpu_idle_ticks});
    try w.print("        \"load1_centis\": {d},\n", .{sample_max.load1_centis});
    try w.print("        \"load5_centis\": {d},\n", .{sample_max.load5_centis});
    try w.print("        \"load15_centis\": {d},\n", .{sample_max.load15_centis});
    try w.print("        \"procs_running\": {d},\n", .{sample_max.procs_running});
    try w.print("        \"procs_blocked\": {d},\n", .{sample_max.procs_blocked});
    try w.print("        \"mem_available_kb\": \"{d}\",\n", .{sample_max.mem_available_kb});
    try w.print("        \"io_read_bytes\": \"{d}\",\n", .{sample_max.io_read_bytes});
