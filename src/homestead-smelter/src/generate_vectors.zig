//! Golden test vector generator for the homestead-smelter binary protocol.
//!
//! Encodes canonical frame instances using the Zig reference encoder and
//! outputs JSON with hex-encoded wire bytes and expected field values.
//! Other language implementations (TypeScript, Go) validate their decoders
//! against the same checked-in file.
//!
//! Regenerate:
//!   cd homestead-smelter && zig build run-generate-vectors > protocol/vectors.json
//!
//! The staleness test (`zig build test`) asserts that the checked-in
//! vectors.json matches the output of this generator.

const std = @import("std");
const hs = @import("homestead_smelter");
const hostp = @import("host_proto.zig");
const hostc = @import("host_core.zig");

const Writer = std.Io.Writer;

// ---------------------------------------------------------------------------
// Canonical test values
// ---------------------------------------------------------------------------

const hello_boot_id_str = "5691d566-f1a6-4342-8604-205e83785b21";
const job_id_str = "4ea0c4ce-4ca7-4389-9c89-612578239c8d";

fn canonicalHello() hs.HelloFrame {
    return .{
        .seq = 0,
        .flags = 0,
        .mono_ns = 1_000_000_000,
        .wall_ns = 1_700_000_000_000_000_000,
        .boot_id = hs.parseUuid(hello_boot_id_str) catch unreachable,
        .mem_total_kb = 516_096,
    };
}

fn canonicalSample() hs.SampleFrame {
    return .{
        .seq = 42,
        .flags = 0,
        .mono_ns = 1_700_000_000,
        .wall_ns = 1_700_000_000_700_000_000,
        .cpu_user_ticks = 123_456,
        .cpu_system_ticks = 78_901,
