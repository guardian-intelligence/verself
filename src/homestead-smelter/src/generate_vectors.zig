    try w.writeAll("      \"fields\": {\n");
    try w.writeAll("        \"kind\": 1\n");
    try w.writeAll("      }\n");
    try w.writeAll("    },\n");

    // -- host_packet_hello --

    try w.writeAll("    \"host_packet_hello\": {\n");
    try w.writeAll("      \"layer\": \"host\",\n");
    try w.writeAll("      \"type\": \"packet\",\n");
    try w.writeAll("      \"hex\": \"");
    try writeHex(w, &packet_hello_bytes);
    try w.writeAll("\",\n");
    try w.writeAll("      \"fields\": {\n");
    try w.print("        \"kind\": {d},\n", .{@intFromEnum(packet_hello.header.kind)});
    try w.print("        \"host_seq\": \"{d}\",\n", .{packet_hello.header.host_seq});
    try w.print("        \"observed_wall_ns\": \"{d}\",\n", .{packet_hello.header.observed_wall_ns});
    try w.print("        \"job_id\": \"{s}\",\n", .{job_id_str});
    try w.print("        \"stream_generation\": {d},\n", .{packet_hello.header.stream_generation});
    try w.print("        \"flags\": {d}\n", .{packet_hello.header.flags});
    try w.writeAll("      },\n");
    try w.writeAll("      \"payload_type\": \"guest_hello\",\n");
    try w.writeAll("      \"payload_fields\": {\n");
    try w.print("        \"seq\": {d},\n", .{hello.seq});
    try w.print("        \"flags\": {d},\n", .{hello.flags});
    try w.print("        \"mono_ns\": \"{d}\",\n", .{hello.mono_ns});
    try w.print("        \"wall_ns\": \"{d}\",\n", .{hello.wall_ns});
    try w.print("        \"boot_id\": \"{s}\",\n", .{hello_boot_id_str});
    try w.print("        \"mem_total_kb\": \"{d}\"\n", .{hello.mem_total_kb});
    try w.writeAll("      }\n");
    try w.writeAll("    },\n");

    // -- host_packet_disconnect --

    try w.writeAll("    \"host_packet_disconnect\": {\n");
    try w.writeAll("      \"layer\": \"host\",\n");
    try w.writeAll("      \"type\": \"packet\",\n");
    try w.writeAll("      \"hex\": \"");
    try writeHex(w, &packet_disconnect_bytes);
    try w.writeAll("\",\n");
    try w.writeAll("      \"fields\": {\n");
    try w.print("        \"kind\": {d},\n", .{@intFromEnum(packet_disconnect.header.kind)});
    try w.print("        \"host_seq\": \"{d}\",\n", .{packet_disconnect.header.host_seq});
    try w.print("        \"observed_wall_ns\": \"{d}\",\n", .{packet_disconnect.header.observed_wall_ns});
    try w.print("        \"job_id\": \"{s}\",\n", .{job_id_str});
    try w.print("        \"stream_generation\": {d},\n", .{packet_disconnect.header.stream_generation});
    try w.print("        \"flags\": {d}\n", .{packet_disconnect.header.flags});
    try w.writeAll("      },\n");
    try w.writeAll("      \"payload_type\": \"disconnect\",\n");
    try w.writeAll("      \"payload_fields\": {\n");
    try w.writeAll("        \"reason\": 2\n");
    try w.writeAll("      }\n");
    try w.writeAll("    }\n");

    // -- close --

    try w.writeAll("  }\n");
    try w.writeAll("}\n");
}

fn writeHex(w: *Writer, bytes: []const u8) !void {
    const hex_chars = "0123456789abcdef";
    for (bytes) |byte| {
        try w.writeByte(hex_chars[byte >> 4]);
        try w.writeByte(hex_chars[byte & 0xf]);
    }
}

// ---------------------------------------------------------------------------
// Entry point (zig build run-generate-vectors)
// ---------------------------------------------------------------------------

pub fn main() !void {
    var buf: [16384]u8 = undefined;
    var fw = std.fs.File.stdout().writer(&buf);
    try writeVectors(&fw.interface);
    try fw.interface.flush();
}

// ---------------------------------------------------------------------------
// Staleness test (zig build test)
// ---------------------------------------------------------------------------

test "vectors.json matches canonical encoder output" {
    const file = try std.fs.cwd().openFile("protocol/vectors.json", .{});
    defer file.close();
    const checked_in = try file.readToEndAlloc(std.testing.allocator, 64 * 1024);
    defer std.testing.allocator.free(checked_in);

    var buf: [32768]u8 = undefined;
    var w = Writer.fixed(&buf);
    try writeVectors(&w);
    try std.testing.expectEqualStrings(checked_in, w.buffered());
}
