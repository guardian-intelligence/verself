#!/usr/bin/env python3
"""Compile the CUE topology into generated deploy inputs."""

from __future__ import annotations

import argparse
import difflib
import hashlib
import json
import os
import subprocess
import sys
from pathlib import Path

import yaml


REPO_ROOT = Path(__file__).resolve().parents[3]
TOPOLOGY_DIR = REPO_ROOT / "src/platform/topology"
TOPOLOGY_INSTANCE = "./instances/local:topology"
GENERATED_TOPOLOGY = REPO_ROOT / "src/platform/ansible/group_vars/all/generated/topology.yml"
HEADER = """# Code generated by src/platform/scripts/topology.py; DO NOT EDIT.
# Source: src/platform/topology/instances/local
---
"""
SPAN_EVENTS: list[dict[str, object]] = []


def rel(path: Path) -> str:
    return str(path.relative_to(REPO_ROOT))


def sha256(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8")).hexdigest()


def span_attrs(extra: dict[str, object] | None = None) -> dict[str, object]:
    attrs: dict[str, object] = {
        "topology.instance": "local",
        "topology.module": rel(TOPOLOGY_DIR),
        "topology.generated_ansible": rel(GENERATED_TOPOLOGY),
    }
    for env_key, attr_key in (
        ("VERSELF_DEPLOY_ID", "verself.deploy_id"),
        ("VERSELF_DEPLOY_RUN_KEY", "verself.deploy_run_key"),
        ("VERSELF_DEPLOY_KIND", "verself.deploy_kind"),
        ("VERSELF_VERIFICATION_RUN", "verself.verification_run"),
    ):
        value = os.environ.get(env_key, "").strip()
        if value:
            attrs[attr_key] = value
    if extra:
        attrs.update(extra)
    return attrs


def record_span(name: str, attrs: dict[str, object] | None = None) -> None:
    SPAN_EVENTS.append({"name": name, "attributes": span_attrs(attrs)})


def flush_spans() -> None:
    if not SPAN_EVENTS:
        return
    env = os.environ.copy()
    env["PROOF_SPAN_SERVICE"] = "topology-compiler"
    env["PROOF_SPANS_JSON"] = json.dumps(SPAN_EVENTS, sort_keys=True)
    run(["go", "run", "./src/otel/cmd/proof-span"], cwd=REPO_ROOT, env=env, capture=True)


def run(
    cmd: list[str],
    *,
    cwd: Path,
    env: dict[str, str] | None = None,
    capture: bool = False,
) -> subprocess.CompletedProcess[str]:
    proc = subprocess.run(
        cmd,
        cwd=cwd,
        env=env,
        text=True,
        stdout=subprocess.PIPE if capture else None,
        stderr=subprocess.PIPE if capture else None,
        check=False,
    )
    if proc.returncode != 0:
        if capture:
            if proc.stdout:
                sys.stdout.write(proc.stdout)
            if proc.stderr:
                sys.stderr.write(proc.stderr)
        raise SystemExit(proc.returncode)
    return proc


def compile_ansible() -> str:
    run(["cue", "fmt", "--check", "--files", "."], cwd=TOPOLOGY_DIR, capture=True)
    record_span("topology.cue.fmt_check")

    run(["cue", "vet", "-c=false", "./..."], cwd=TOPOLOGY_DIR, capture=True)
    record_span("topology.cue.vet_schema")

    run(["cue", "vet", "-c", "./instances/..."], cwd=TOPOLOGY_DIR, capture=True)
    record_span("topology.cue.vet_instance")

    proc = run(
        ["cue", "export", TOPOLOGY_INSTANCE, "-e", "ansible", "--out", "json"],
        cwd=TOPOLOGY_DIR,
        capture=True,
    )
    body = yaml.safe_dump(
        json.loads(proc.stdout),
        default_flow_style=False,
        sort_keys=True,
    )
    generated = HEADER + body
    record_span(
        "topology.cue.export_ansible",
        {"topology.generated_sha256": sha256(generated)},
    )
    return generated


def check(generated: str) -> int:
    actual = GENERATED_TOPOLOGY.read_text(encoding="utf-8") if GENERATED_TOPOLOGY.exists() else ""
    fresh = actual == generated
    record_span(
        "topology.ansible.generated_check",
        {
            "topology.generated_fresh": fresh,
            "topology.generated_sha256": sha256(generated),
            "topology.current_sha256": sha256(actual) if actual else "",
        },
    )
    if fresh:
        return 0

    diff = difflib.unified_diff(
        actual.splitlines(keepends=True),
        generated.splitlines(keepends=True),
        fromfile=rel(GENERATED_TOPOLOGY),
        tofile=f"{rel(GENERATED_TOPOLOGY)}.expected",
    )
    sys.stderr.writelines(diff)
    return 1


def generate(generated: str) -> int:
    old = GENERATED_TOPOLOGY.read_text(encoding="utf-8") if GENERATED_TOPOLOGY.exists() else ""
    GENERATED_TOPOLOGY.parent.mkdir(parents=True, exist_ok=True)
    if old != generated:
        GENERATED_TOPOLOGY.write_text(generated, encoding="utf-8")
    record_span(
        "topology.ansible.generated_generate",
        {
            "topology.generated_changed": old != generated,
            "topology.generated_sha256": sha256(generated),
        },
    )
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(description="Compile CUE topology artifacts.")
    parser.add_argument("command", choices=("check", "generate", "print"))
    args = parser.parse_args()

    generated = compile_ansible()
    if args.command == "check":
        rc = check(generated)
    elif args.command == "generate":
        rc = generate(generated)
    else:
        sys.stdout.write(generated)
        rc = 0

    flush_spans()
    return rc


if __name__ == "__main__":
    raise SystemExit(main())
