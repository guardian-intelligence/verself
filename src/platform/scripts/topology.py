#!/usr/bin/env python3
"""Compile the CUE topology graph into typed deploy inputs."""

from __future__ import annotations

import argparse
import difflib
import hashlib
import json
import os
import subprocess
import sys
from dataclasses import dataclass
from pathlib import Path

import yaml


REPO_ROOT = Path(__file__).resolve().parents[3]
TOPOLOGY_DIR = REPO_ROOT / "src/platform/topology"
TOPOLOGY_INSTANCE = "./instances/local:topology"
GENERATED_DIR = REPO_ROOT / "src/platform/ansible/group_vars/all/generated"
HEADER = """# Code generated by src/platform/scripts/topology.py; DO NOT EDIT.
# Source: src/platform/topology/instances/local
---
"""

CONTROL_PLANE_COMPONENTS = {
    "billing",
    "company",
    "console",
    "governance_service",
    "identity_service",
    "mailbox_service",
    "notifications_service",
    "object_storage_service",
    "platform",
    "profile_service",
    "projects_service",
    "sandbox_rental",
    "secrets_service",
    "source_code_hosting_service",
}
CONTROL_PLANE_MIN_PORT = 4240
CONTROL_PLANE_MAX_PORT = 4269


@dataclass(frozen=True)
class Artifact:
    name: str
    expression: str
    path: Path


ARTIFACTS = (
    Artifact("runtime", "outputs.runtime", GENERATED_DIR / "runtime.yml"),
    Artifact("endpoints", "outputs.endpoints", GENERATED_DIR / "endpoints.yml"),
    Artifact("clusters", "outputs.clusters", GENERATED_DIR / "clusters.yml"),
    Artifact("routes", "outputs.routes", GENERATED_DIR / "routes.yml"),
    Artifact("dns", "outputs.dns", GENERATED_DIR / "dns.yml"),
    Artifact("nftables", "outputs.nftables", GENERATED_DIR / "nftables.yml"),
    Artifact("spire", "outputs.spire", GENERATED_DIR / "spire.yml"),
    Artifact("postgres", "outputs.postgres", GENERATED_DIR / "postgres.yml"),
    Artifact("deploy", "outputs.deploy", GENERATED_DIR / "deploy.yml"),
    Artifact("proof", "outputs.proof", GENERATED_DIR / "proof.yml"),
)

SPAN_EVENTS: list[dict[str, object]] = []


def rel(path: Path) -> str:
    return str(path.relative_to(REPO_ROOT))


def sha256(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8")).hexdigest()


def span_attrs(extra: dict[str, object] | None = None) -> dict[str, object]:
    attrs: dict[str, object] = {
        "topology.instance": "local",
        "topology.module": rel(TOPOLOGY_DIR),
        "topology.generated_dir": rel(GENERATED_DIR),
    }
    for env_key, attr_key in (
        ("VERSELF_DEPLOY_ID", "verself.deploy_id"),
        ("VERSELF_DEPLOY_RUN_KEY", "verself.deploy_run_key"),
        ("VERSELF_DEPLOY_KIND", "verself.deploy_kind"),
        ("VERSELF_VERIFICATION_RUN", "verself.verification_run"),
    ):
        value = os.environ.get(env_key, "").strip()
        if value:
            attrs[attr_key] = value
    if extra:
        attrs.update(extra)
    return attrs


def record_span(name: str, attrs: dict[str, object] | None = None) -> None:
    SPAN_EVENTS.append({"name": name, "attributes": span_attrs(attrs)})


def flush_spans() -> None:
    if not SPAN_EVENTS:
        return
    env = os.environ.copy()
    env["PROOF_SPAN_SERVICE"] = "topology-compiler"
    env["PROOF_SPANS_JSON"] = json.dumps(SPAN_EVENTS, sort_keys=True)
    proc = subprocess.run(
        ["go", "run", "./src/otel/cmd/proof-span"],
        cwd=REPO_ROOT,
        env=env,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        check=False,
    )
    if proc.returncode != 0:
        if proc.stdout:
            sys.stdout.write(proc.stdout)
        if proc.stderr:
            sys.stderr.write(proc.stderr)
        raise SystemExit(proc.returncode)


def run(
    cmd: list[str],
    *,
    cwd: Path,
    env: dict[str, str] | None = None,
    capture: bool = False,
) -> subprocess.CompletedProcess[str]:
    proc = subprocess.run(
        cmd,
        cwd=cwd,
        env=env,
        text=True,
        stdout=subprocess.PIPE if capture else None,
        stderr=subprocess.PIPE if capture else None,
        check=False,
    )
    if proc.returncode != 0:
        record_span(
            "topology.command.error",
            {
                "topology.command": " ".join(cmd),
                "topology.cwd": rel(cwd),
                "topology.exit_code": proc.returncode,
            },
        )
        if capture:
            if proc.stdout:
                sys.stdout.write(proc.stdout)
            if proc.stderr:
                sys.stderr.write(proc.stderr)
        raise SystemExit(proc.returncode)
    return proc


def yaml_document(payload: object) -> str:
    return HEADER + yaml.safe_dump(
        payload,
        default_flow_style=False,
        sort_keys=True,
    )


def load_topology() -> dict[str, object]:
    proc = run(
        ["cue", "export", TOPOLOGY_INSTANCE, "-e", "topology", "--out", "json"],
        cwd=TOPOLOGY_DIR,
        capture=True,
    )
    topology = json.loads(proc.stdout)
    record_span(
        "topology.cue.export_graph",
        {"topology.graph_sha256": sha256(proc.stdout)},
    )
    return topology


def validate_graph(topology: dict[str, object]) -> None:
    components = topology["components"]
    gateways = topology["gateways"]
    routes = topology["routes"]
    edges = topology["edges"]

    assert isinstance(components, dict)
    assert isinstance(gateways, dict)
    assert isinstance(routes, list)
    assert isinstance(edges, list)

    ports: dict[int, str] = {}
    interface_count = 0
    endpoint_count = 0
    for component_name, component in components.items():
        assert isinstance(component, dict)
        endpoints = component["endpoints"]
        interfaces = component["interfaces"]
        assert isinstance(endpoints, dict)
        assert isinstance(interfaces, dict)

        for endpoint_name, endpoint in endpoints.items():
            assert isinstance(endpoint, dict)
            endpoint_count += 1
            port = endpoint["port"]
            assert isinstance(port, int)
            existing = ports.get(port)
            if existing is not None:
                raise ValueError(
                    f"duplicate topology port {port}: {existing} and "
                    f"{component_name}.{endpoint_name}"
                )
            ports[port] = f"{component_name}.{endpoint_name}"
            if component_name in CONTROL_PLANE_COMPONENTS and not (
                CONTROL_PLANE_MIN_PORT <= port <= CONTROL_PLANE_MAX_PORT
            ):
                raise ValueError(
                    f"{component_name}.{endpoint_name} uses port {port}, outside "
                    f"{CONTROL_PLANE_MIN_PORT}-{CONTROL_PLANE_MAX_PORT}"
                )
            if component.get("kind") == "resource" and endpoint.get("exposure") == "public":
                raise ValueError(f"resource endpoint cannot be public: {component_name}.{endpoint_name}")

        for interface_name, interface in interfaces.items():
            assert isinstance(interface, dict)
            interface_count += 1
            endpoint_name = interface["endpoint"]
            if endpoint_name not in endpoints:
                raise ValueError(f"{component_name}.{interface_name} references missing endpoint {endpoint_name}")

    for route in routes:
        assert isinstance(route, dict)
        gateway = route["gateway"]
        if gateway not in gateways:
            raise ValueError(f"route references missing gateway {gateway}")
        target = route["to"]
        assert isinstance(target, dict)
        component_name = target["component"]
        interface_name = target["interface"]
        component = components.get(component_name)
        if not isinstance(component, dict):
            raise ValueError(f"route references missing component {component_name}")
        interfaces = component["interfaces"]
        assert isinstance(interfaces, dict)
        if interface_name not in interfaces:
            raise ValueError(f"route references missing interface {component_name}.{interface_name}")

    for edge in edges:
        assert isinstance(edge, dict)
        source = edge["from"]
        if source not in components:
            raise ValueError(f"edge references missing source component {source}")
        target = edge["to"]
        assert isinstance(target, dict)
        component_name = target["component"]
        interface_name = target["interface"]
        component = components.get(component_name)
        if not isinstance(component, dict):
            raise ValueError(f"edge references missing target component {component_name}")
        interfaces = component["interfaces"]
        assert isinstance(interfaces, dict)
        if interface_name not in interfaces:
            raise ValueError(f"edge references missing target interface {component_name}.{interface_name}")

    record_span(
        "topology.graph.validate",
        {
            "topology.component_count": len(components),
            "topology.endpoint_count": endpoint_count,
            "topology.interface_count": interface_count,
            "topology.route_count": len(routes),
            "topology.edge_count": len(edges),
        },
    )


def compile_artifacts() -> dict[Artifact, str]:
    run(["cue", "fmt", "--check", "--files", "."], cwd=TOPOLOGY_DIR, capture=True)
    record_span("topology.cue.fmt_check")

    run(["cue", "vet", "-c=false", "./..."], cwd=TOPOLOGY_DIR, capture=True)
    record_span("topology.cue.vet_schema")

    run(["cue", "vet", "-c", "./instances/..."], cwd=TOPOLOGY_DIR, capture=True)
    record_span("topology.cue.vet_instance")

    validate_graph(load_topology())

    generated: dict[Artifact, str] = {}
    for artifact in ARTIFACTS:
        proc = run(
            ["cue", "export", TOPOLOGY_INSTANCE, "-e", artifact.expression, "--out", "json"],
            cwd=TOPOLOGY_DIR,
            capture=True,
        )
        content = yaml_document(json.loads(proc.stdout))
        generated[artifact] = content
        record_span(
            "topology.cue.export_artifact",
            {
                "topology.artifact": artifact.name,
                "topology.generated_file": rel(artifact.path),
                "topology.generated_sha256": sha256(content),
            },
        )
    return generated


def check(generated: dict[Artifact, str]) -> int:
    rc = 0
    for artifact, expected in generated.items():
        actual = artifact.path.read_text(encoding="utf-8") if artifact.path.exists() else ""
        fresh = actual == expected
        record_span(
            "topology.generated.freshness_check",
            {
                "topology.artifact": artifact.name,
                "topology.generated_file": rel(artifact.path),
                "topology.generated_fresh": fresh,
                "topology.generated_sha256": sha256(expected),
                "topology.current_sha256": sha256(actual) if actual else "",
            },
        )
        if fresh:
            continue
        rc = 1
        diff = difflib.unified_diff(
            actual.splitlines(keepends=True),
            expected.splitlines(keepends=True),
            fromfile=rel(artifact.path),
            tofile=f"{rel(artifact.path)}.expected",
        )
        sys.stderr.writelines(diff)
    return rc


def generate(generated: dict[Artifact, str]) -> int:
    GENERATED_DIR.mkdir(parents=True, exist_ok=True)
    for artifact, expected in generated.items():
        actual = artifact.path.read_text(encoding="utf-8") if artifact.path.exists() else ""
        changed = actual != expected
        if changed:
            artifact.path.write_text(expected, encoding="utf-8")
        record_span(
            "topology.generated.write",
            {
                "topology.artifact": artifact.name,
                "topology.generated_file": rel(artifact.path),
                "topology.generated_changed": changed,
                "topology.generated_sha256": sha256(expected),
            },
        )
    return 0


def print_artifacts(generated: dict[Artifact, str]) -> int:
    for artifact, content in generated.items():
        sys.stdout.write(f"# {rel(artifact.path)}\n")
        sys.stdout.write(content)
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(description="Compile CUE topology artifacts.")
    parser.add_argument("command", choices=("check", "generate", "print"))
    args = parser.parse_args()

    rc = 1
    try:
        generated = compile_artifacts()
        if args.command == "check":
            rc = check(generated)
        elif args.command == "generate":
            rc = generate(generated)
        else:
            rc = print_artifacts(generated)
    except BaseException as exc:
        record_span(
            "topology.compiler.error",
            {
                "topology.error_type": type(exc).__name__,
                "topology.error": str(exc),
            },
        )
        raise
    finally:
        flush_spans()
    return rc


if __name__ == "__main__":
    raise SystemExit(main())
