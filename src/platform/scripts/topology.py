def compile_artifacts() -> dict[Artifact, str]:
    run(["cue", "fmt", "--check", "--files", "."], cwd=TOPOLOGY_DIR, capture=True)
    record_span("topology.cue.fmt_check")

    run(["cue", "vet", "-c=false", "./..."], cwd=TOPOLOGY_DIR, capture=True)
    record_span("topology.cue.vet_schema")

    run(["cue", "vet", "-c", "./instances/..."], cwd=TOPOLOGY_DIR, capture=True)
    record_span("topology.cue.vet_instance")

    topology = load_topology()
    config = load_config()
    catalog = load_catalog()
    validate_graph(topology)
    payloads = render_artifact_payloads(topology, config, catalog)

    generated: dict[Artifact, str] = {}
    for artifact in ARTIFACTS:
        content = yaml_document(payloads[artifact.name])
        generated[artifact] = content
        record_span(
            "topology.generated.render_artifact",
            {
                "topology.artifact": artifact.name,
                "topology.generated_file": rel(artifact.path),
                "topology.generated_sha256": sha256(content),
            },
        )
    return generated


def check(generated: dict[Artifact, str]) -> int:
    rc = 0
    for artifact, expected in generated.items():
        actual = artifact.path.read_text(encoding="utf-8") if artifact.path.exists() else ""
        fresh = actual == expected
        record_span(
            "topology.generated.freshness_check",
            {
                "topology.artifact": artifact.name,
                "topology.generated_file": rel(artifact.path),
                "topology.generated_fresh": fresh,
                "topology.generated_sha256": sha256(expected),
                "topology.current_sha256": sha256(actual) if actual else "",
            },
        )
        if fresh:
            continue
        rc = 1
        diff = difflib.unified_diff(
            actual.splitlines(keepends=True),
            expected.splitlines(keepends=True),
            fromfile=rel(artifact.path),
            tofile=f"{rel(artifact.path)}.expected",
        )
        sys.stderr.writelines(diff)
    return rc


def generate(generated: dict[Artifact, str]) -> int:
    GENERATED_DIR.mkdir(parents=True, exist_ok=True)
    for artifact, expected in generated.items():
        actual = artifact.path.read_text(encoding="utf-8") if artifact.path.exists() else ""
        changed = actual != expected
        if changed:
            artifact.path.write_text(expected, encoding="utf-8")
        record_span(
            "topology.generated.write",
            {
                "topology.artifact": artifact.name,
                "topology.generated_file": rel(artifact.path),
                "topology.generated_changed": changed,
                "topology.generated_sha256": sha256(expected),
            },
        )
    return 0


def print_artifacts(generated: dict[Artifact, str]) -> int:
    for artifact, content in generated.items():
        sys.stdout.write(f"# {rel(artifact.path)}\n")
        sys.stdout.write(content)
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(description="Compile CUE topology artifacts.")
    parser.add_argument("command", choices=("check", "generate", "print"))
    args = parser.parse_args()

    rc = 1
    try:
        generated = compile_artifacts()
        if args.command == "check":
            rc = check(generated)
        elif args.command == "generate":
            rc = generate(generated)
        else:
            rc = print_artifacts(generated)
    except BaseException as exc:
        record_span(
            "topology.compiler.error",
            {
                "topology.error_type": type(exc).__name__,
                "topology.error": str(exc),
            },
        )
        raise
    finally:
        flush_spans()
    return rc


if __name__ == "__main__":
    raise SystemExit(main())
