class NoAliasDumper(yaml.SafeDumper):
    def ignore_aliases(self, data: object) -> bool:
        return True


def rel(path: Path) -> str:
    return str(path.relative_to(REPO_ROOT))


def sha256(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8")).hexdigest()


def span_attrs(extra: dict[str, object] | None = None) -> dict[str, object]:
    attrs: dict[str, object] = {
        "topology.instance": "local",
        "topology.module": rel(TOPOLOGY_DIR),
        "topology.generated_dir": rel(GENERATED_DIR),
    }
    for env_key, attr_key in (
        ("VERSELF_DEPLOY_ID", "verself.deploy_id"),
        ("VERSELF_DEPLOY_RUN_KEY", "verself.deploy_run_key"),
        ("VERSELF_DEPLOY_KIND", "verself.deploy_kind"),
        ("VERSELF_VERIFICATION_RUN", "verself.verification_run"),
    ):
        value = os.environ.get(env_key, "").strip()
        if value:
            attrs[attr_key] = value
    if extra:
        attrs.update(extra)
    return attrs


def record_span(name: str, attrs: dict[str, object] | None = None) -> None:
    SPAN_EVENTS.append({"name": name, "attributes": span_attrs(attrs)})


def flush_spans() -> None:
    if not SPAN_EVENTS:
        return
    env = os.environ.copy()
    env["PROOF_SPAN_SERVICE"] = "topology-compiler"
    env["PROOF_SPANS_JSON"] = json.dumps(SPAN_EVENTS, sort_keys=True)
    proc = subprocess.run(
        ["go", "run", "./cmd/proof-span"],
        cwd=REPO_ROOT / "src" / "otel",
        env=env,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        check=False,
    )
    if proc.returncode != 0:
        if proc.stdout:
            sys.stdout.write(proc.stdout)
        if proc.stderr:
            sys.stderr.write(proc.stderr)
        raise SystemExit(proc.returncode)


def run(
    cmd: list[str],
    *,
    cwd: Path,
    env: dict[str, str] | None = None,
    capture: bool = False,
) -> subprocess.CompletedProcess[str]:
    proc = subprocess.run(
        cmd,
        cwd=cwd,
        env=env,
        text=True,
        stdout=subprocess.PIPE if capture else None,
        stderr=subprocess.PIPE if capture else None,
        check=False,
    )
    if proc.returncode != 0:
        record_span(
            "topology.command.error",
            {
                "topology.command": " ".join(cmd),
                "topology.cwd": rel(cwd),
                "topology.exit_code": proc.returncode,
            },
        )
        if capture:
            if proc.stdout:
                sys.stdout.write(proc.stdout)
            if proc.stderr:
                sys.stderr.write(proc.stderr)
        raise SystemExit(proc.returncode)
    return proc


def yaml_document(payload: object) -> str:
    return HEADER + yaml.dump(
        payload,
        Dumper=NoAliasDumper,
        default_flow_style=False,
        sort_keys=True,
        width=4096,
    )


def validate_artifact(owner: str, artifact: object) -> None:
    assert isinstance(artifact, dict)
    kind = artifact["kind"]
    assert isinstance(kind, str)
    output = artifact["output"]
    assert isinstance(output, str)
    if kind != "none" and not output:
        raise ValueError(f"{owner} artifact is missing output")
    if kind == "go_binary" and not artifact.get("bazel_label"):
        raise ValueError(f"{owner} go_binary artifact is missing bazel_label")


def cue_export(package: str, expression: str) -> tuple[object, str]:
    proc = run(
        ["cue", "export", package, "-e", expression, "--out", "json"],
        cwd=TOPOLOGY_DIR,
        capture=True,
    )
    return json.loads(proc.stdout), proc.stdout


