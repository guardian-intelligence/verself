//! Golden test vector generator for the vm-guest-telemetry binary protocol.
//!
//! Encodes canonical frame instances using the Zig reference encoder and
//! outputs JSON with hex-encoded wire bytes and expected field values.
//! Other language implementations (TypeScript, Go) validate their decoders
//! against the same checked-in file.
//!
//! Regenerate:
//!   cd vm-guest-telemetry && zig build run-generate-vectors > protocol/vectors.json
//!
//! The staleness test (`zig build test`) asserts that the checked-in
//! vectors.json matches the output of this generator.

const std = @import("std");
const hs = @import("vm_guest_telemetry");

const Writer = std.Io.Writer;

// ---------------------------------------------------------------------------
// JSON shape
// ---------------------------------------------------------------------------

/// Wire-format wrapper: serialize a u64 as a quoted decimal-digit string. JS
/// `Number` cannot losslessly hold values above 2^53, so the vectors.json spec
/// uniformly encodes every u64 field as a string regardless of magnitude —
/// decoders in any language pick the field type from the schema, not by
/// sniffing the JSON token.
const U64String = struct {
    value: u64,

    pub fn jsonStringify(self: U64String, jws: anytype) !void {
        try jws.print("\"{d}\"", .{self.value});
    }
};

/// Wire-format wrapper: serialize a fixed-size frame as a lowercase-hex JSON
/// string. The array size is comptime-known, so `std.fmt.bytesToHex` returns
/// the encoding as a stack-allocated array and we hand that to the JSON writer.
const HexBytes = struct {
    bytes: *const [hs.frame_size]u8,

    pub fn jsonStringify(self: HexBytes, jws: anytype) !void {
        const hex = std.fmt.bytesToHex(self.bytes, .lower);
        try jws.write(&hex);
    }
};

const Protocol = struct {
    guest_frame_size: u32,
    byte_order: []const u8,
};

const HelloFields = struct {
    seq: u32,
    flags: u32,
    mono_ns: U64String,
    wall_ns: U64String,
    boot_id: []const u8,
    mem_total_kb: U64String,
};

const SampleFields = struct {
    seq: u32,
    flags: u32,
    mono_ns: U64String,
    wall_ns: U64String,
    cpu_user_ticks: U64String,
    cpu_system_ticks: U64String,
    cpu_idle_ticks: U64String,
    load1_centis: u32,
    load5_centis: u32,
    load15_centis: u32,
    procs_running: u16,
    procs_blocked: u16,
    mem_available_kb: U64String,
    io_read_bytes: U64String,
    io_write_bytes: U64String,
    net_rx_bytes: U64String,
    net_tx_bytes: U64String,
    psi_cpu_pct100: u16,
    psi_mem_pct100: u16,
    psi_io_pct100: u16,
};

const HelloVector = struct {
    layer: []const u8 = "guest",
    type: []const u8 = "hello",
    hex: HexBytes,
    fields: HelloFields,
};

// Field-declaration order here is the on-disk JSON key order: layer, type,
// optional note, hex, fields. `emit_null_optional_fields = false` drops the
// note entirely when unset so the non-max sample vector stays lean.
const SampleVector = struct {
    layer: []const u8 = "guest",
    type: []const u8 = "sample",
    note: ?[]const u8 = null,
    hex: HexBytes,
    fields: SampleFields,
};

const Vectors = struct {
    guest_hello: HelloVector,
    guest_sample: SampleVector,
    guest_sample_max: SampleVector,
};

const Document = struct {
    schema_version: u32 = 1,
    note: []const u8 = "Generated by: zig build run-generate-vectors. Do not edit.",
    protocol: Protocol = .{ .guest_frame_size = 128, .byte_order = "little-endian" },
    u64_encoding: []const u8 = "decimal-string",
    vectors: Vectors,
};

