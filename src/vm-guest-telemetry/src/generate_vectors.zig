// ---------------------------------------------------------------------------
// JSON generation
// ---------------------------------------------------------------------------

// The encoded frame byte buffers live inside the Document by reference; they
// must outlive the call to std.json.Stringify. Hold them in locals on the
// caller stack and pass pointers.
const Encoded = struct {
    hello_bytes: [hs.frame_size]u8,
    sample_bytes: [hs.frame_size]u8,
    sample_max_bytes: [hs.frame_size]u8,
    hello: hs.HelloFrame,
    sample: hs.SampleFrame,
    sample_max: hs.SampleFrame,
};

fn encodeCanonical() Encoded {
    const hello = canonicalHello();
    const sample = canonicalSample();
    const sample_max = canonicalSampleMax();
    return .{
        .hello_bytes = hs.encodeHelloFrame(hello),
        .sample_bytes = hs.encodeSampleFrame(sample),
        .sample_max_bytes = hs.encodeSampleFrame(sample_max),
        .hello = hello,
        .sample = sample,
        .sample_max = sample_max,
    };
}

fn buildDocument(e: *const Encoded) Document {
    return .{
        .vectors = .{
            .guest_hello = .{
                .hex = .{ .bytes = &e.hello_bytes },
                .fields = helloFields(e.hello),
            },
            .guest_sample = .{
                .hex = .{ .bytes = &e.sample_bytes },
                .fields = sampleFields(e.sample),
            },
            .guest_sample_max = .{
                .note = "All fields at maximum value. Tests u64/u32/u16 boundary handling.",
                .hex = .{ .bytes = &e.sample_max_bytes },
                .fields = sampleFields(e.sample_max),
            },
        },
    };
}

pub fn writeVectors(w: *Writer) !void {
    const encoded = encodeCanonical();
    const doc = buildDocument(&encoded);
    try std.json.Stringify.value(doc, .{
        .whitespace = .indent_2,
        .emit_null_optional_fields = false,
    }, w);
    try w.writeByte('\n');
}

// ---------------------------------------------------------------------------
// Entry point (zig build run-generate-vectors)
// ---------------------------------------------------------------------------

pub fn main() !void {
    var buf: [16384]u8 = undefined;
    var fw = std.fs.File.stdout().writer(&buf);
    try writeVectors(&fw.interface);
    try fw.interface.flush();
}

// ---------------------------------------------------------------------------
// Staleness test (zig build test)
// ---------------------------------------------------------------------------

test "vectors.json matches canonical encoder output" {
    const file = try std.fs.cwd().openFile("protocol/vectors.json", .{});
    defer file.close();
    const checked_in = try file.readToEndAlloc(std.testing.allocator, 64 * 1024);
    defer std.testing.allocator.free(checked_in);

    var buf: [32768]u8 = undefined;
    var w = Writer.fixed(&buf);
    try writeVectors(&w);
    try std.testing.expectEqualStrings(checked_in, w.buffered());
}
