func expr(root cue.Value, path string) (ast.Expr, error) {
	value := root.LookupPath(cue.ParsePath(path))
	if err := value.Err(); err != nil {
		return nil, fmt.Errorf("lookup %s: %w", path, err)
	}
	if !value.Exists() {
		return nil, fmt.Errorf("lookup %s: value does not exist", path)
	}
	if err := value.Validate(cue.Concrete(true)); err != nil {
		return nil, fmt.Errorf("validate %s: %w", path, err)
	}
	expr, ok := value.Syntax(cue.Concrete(true)).(ast.Expr)
	if !ok {
		return nil, fmt.Errorf("lookup %s: CUE syntax is %T, not ast.Expr", path, value.Syntax())
	}
	return expr, nil
}

func field(name string, value ast.Expr) *ast.Field {
	return &ast.Field{Label: ast.NewIdent(name), Value: value}
}
