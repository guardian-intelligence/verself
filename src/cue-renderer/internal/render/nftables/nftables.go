const generatedHeader = "# Code generated by src/cue-renderer; DO NOT EDIT.\n"

type Renderer struct{}

func (Renderer) Name() string { return "nftables" }

func (Renderer) Render(_ context.Context, loaded load.Loaded, out render.WritableFS) error {
	conf, err := renderMainConfig()
	if err != nil {
		return err
	}
	if err := out.WriteFile(projection.RenderedPath("etc/nftables.conf"), conf); err != nil {
		return err
	}

	hostFirewall, err := renderHostFirewall(loaded)
	if err != nil {
		return err
	}
	if err := out.WriteFile(projection.RenderedPath("etc/nftables.d/host-firewall.nft"), hostFirewall); err != nil {
		return err
	}

	target, err := renderFirewallTarget()
	if err != nil {
		return err
	}
	if err := out.WriteFile(projection.RenderedPath("etc/systemd/system/verself-firewall.target"), target); err != nil {
		return err
	}

	files, err := renderRulesets(loaded)
	if err != nil {
		return err
	}
	for target, data := range files {
		if err := out.WriteFile(projection.RenderedPath(target), data); err != nil {
			return err
		}
	}
	return nil
}

func renderMainConfig() ([]byte, error) {
	return []byte(generatedHeader + `#!/usr/sbin/nft -f
# Load all rulesets from /etc/nftables.d/.
#
# No "flush ruleset": each .nft file does its own atomic table replace, and
# provider rules such as Latitude.sh metadata DNAT are left untouched.
include "/etc/nftables.d/*.nft"
`), nil
}

type hostFirewallConfig struct {
	TrustedInterfaces []string
	PublicTCPPorts    []int
	PublicUDPPorts    []int
	GuestTCPPorts     []int
	GuestCIDR         string
	GuestHostIP       string
	SSHPublic         bool
	SSHRate           string
	SSHBurst          int
}

func renderHostFirewall(loaded load.Loaded) ([]byte, error) {
	cfg, err := hostFirewallConfigFromLoaded(loaded)
	if err != nil {
		return nil, err
	}

	var b strings.Builder
	b.WriteString(generatedHeader)
	b.WriteString(`#!/usr/sbin/nft -f
# Host firewall: default-deny ingress with allowlisted services.

table inet verself_host
delete table inet verself_host

table inet verself_host {
    chain input {
        type filter hook input priority filter; policy drop;

        # Existing connections.
        ct state established,related accept
        ct state invalid drop

        # Trusted interfaces: loopback and WireGuard meshes.
`)
	for _, iface := range cfg.TrustedInterfaces {
		fmt.Fprintf(&b, "        iifname %q accept\n", iface)
	}

	if len(cfg.GuestTCPPorts) > 0 {
		b.WriteString(`
        # Firecracker guests may reach selected services on the host-only service plane.
`)
		fmt.Fprintf(&b, "        iifname \"fc-tap-*\" ip saddr %s ip daddr %s tcp dport %s accept\n", cfg.GuestCIDR, cfg.GuestHostIP, portSet(cfg.GuestTCPPorts))
	}

	b.WriteString(`
        # ICMP / ICMPv6 (ping, PMTUD, neighbor discovery).
        ip protocol icmp accept
        ip6 nexthdr icmpv6 accept
`)

	if len(cfg.PublicTCPPorts) > 0 {
		b.WriteString(`
        # Public TCP services.
`)
		fmt.Fprintf(&b, "        tcp dport %s accept\n", portSet(cfg.PublicTCPPorts))
	}

	if len(cfg.PublicUDPPorts) > 0 {
		b.WriteString(`
        # Public UDP services.
`)
		fmt.Fprintf(&b, "        udp dport %s accept\n", portSet(cfg.PublicUDPPorts))
	}

	if cfg.SSHPublic {
		b.WriteString(`
        # SSH from the public internet with per-source-IP rate limiting.
`)
		fmt.Fprintf(&b, "        tcp dport 22 ct state new meter ssh_rate { ip saddr limit rate %s burst %d packets } accept\n", cfg.SSHRate, cfg.SSHBurst)
	}

	b.WriteString(`
        # Everything else hits policy drop.
    }
}
`)
	return []byte(b.String()), nil
}

func renderFirewallTarget() ([]byte, error) {
	return []byte(generatedHeader + `# verself-firewall.target is the host firewall readiness point.
#
# Network-facing verself services declare After= and Requires= on this target so
# they cannot bind before nftables has installed the default-deny policy.

[Unit]
Description=verself host firewall ready
Requires=nftables.service
After=nftables.service

[Install]
WantedBy=multi-user.target
`), nil
}

func hostFirewallConfigFromLoaded(loaded load.Loaded) (hostFirewallConfig, error) {
	nftCfg, err := projection.ConfigMap(loaded, "nftables")
	if err != nil {
		return hostFirewallConfig{}, err
	}
	firecrackerCfg, err := projection.ConfigMap(loaded, "firecracker")
	if err != nil {
		return hostFirewallConfig{}, err
	}
	guestCIDR, err := projection.String(firecrackerCfg, "config.firecracker", "guest_pool_cidr")
	if err != nil {
		return hostFirewallConfig{}, err
	}
	sshCfg, err := projection.Map(nftCfg, "config.nftables", "ssh")
	if err != nil {
		return hostFirewallConfig{}, err
	}
	sshRate, err := projection.String(sshCfg, "config.nftables.ssh", "rate")
	if err != nil {
		return hostFirewallConfig{}, err
	}
	sshBurst, err := projection.Int(sshCfg, "config.nftables.ssh", "burst")
	if err != nil {
		return hostFirewallConfig{}, err
	}
	sshPublic, err := boolValue(sshCfg, "config.nftables.ssh", "public")
	if err != nil {
		return hostFirewallConfig{}, err
	}

	publicTCP := map[int]bool{}
	publicTCPRaw, err := projection.Slice(nftCfg, "config.nftables", "public_tcp_ports")
	if err != nil {
		return hostFirewallConfig{}, err
	}
	for i, raw := range publicTCPRaw {
		port, err := intValue(raw, fmt.Sprintf("config.nftables.public_tcp_ports[%d]", i))
		if err != nil {
			return hostFirewallConfig{}, err
		}
		publicTCP[port] = true
	}

	publicUDP := map[int]bool{}
	trustedInterfaces := map[string]bool{"lo": true}
	wireguard, err := projection.ConfigMap(loaded, "wireguard")
	if err != nil {
		return hostFirewallConfig{}, err
	}
	tunnels, err := projection.Map(wireguard, "config.wireguard", "tunnels")
	if err != nil {
		return hostFirewallConfig{}, err
	}
	for _, name := range projection.SortedKeys(tunnels) {
		tunnel, ok := tunnels[name].(map[string]any)
		if !ok {
			return hostFirewallConfig{}, fmt.Errorf("config.wireguard.tunnels.%s: expected map, got %T", name, tunnels[name])
		}
		iface, err := projection.String(tunnel, "config.wireguard.tunnels."+name, "interface")
		if err != nil {
			return hostFirewallConfig{}, err
		}
		port, err := projection.Int(tunnel, "config.wireguard.tunnels."+name, "port")
		if err != nil {
			return hostFirewallConfig{}, err
		}
		trustedInterfaces[iface] = true
		publicUDP[int(port)] = true
	}

	components, err := projection.Components(loaded)
	if err != nil {
		return hostFirewallConfig{}, err
	}
	guestTCP := map[int]bool{}
	var guestHostIP string
	for _, component := range components {
		if component.Name == "firecracker_host_service" {
			guestHostIP, err = projection.String(component.Value, component.Name, "host")
			if err != nil {
				return hostFirewallConfig{}, err
			}
		}
		endpoints, err := projection.NestedFields(component, "endpoints")
		if err != nil {
			return hostFirewallConfig{}, err
		}
		for _, endpoint := range endpoints {
			exposure, err := projection.String(endpoint.Value, component.Name+".endpoints."+endpoint.Name, "exposure")
			if err != nil {
				return hostFirewallConfig{}, err
			}
			protocol, err := projection.String(endpoint.Value, component.Name+".endpoints."+endpoint.Name, "protocol")
			if err != nil {
				return hostFirewallConfig{}, err
			}
			port, err := projection.Int(endpoint.Value, component.Name+".endpoints."+endpoint.Name, "port")
			if err != nil {
				return hostFirewallConfig{}, err
			}
			switch exposure {
			case "public":
				if protocol == "statsd" {
					publicUDP[int(port)] = true
				} else {
					publicTCP[int(port)] = true
				}
			case "guest_host":
				if protocol == "statsd" {
					continue
				}
				guestTCP[int(port)] = true
			}
		}
	}
	if guestHostIP == "" {
		return hostFirewallConfig{}, fmt.Errorf("firecracker_host_service.host is required for guest host firewall rules")
	}

	return hostFirewallConfig{
		TrustedInterfaces: sortedStringSet(trustedInterfaces),
		PublicTCPPorts:    sortedIntSet(publicTCP),
		PublicUDPPorts:    sortedIntSet(publicUDP),
		GuestTCPPorts:     sortedIntSet(guestTCP),
		GuestCIDR:         guestCIDR,
		GuestHostIP:       guestHostIP,
		SSHPublic:         sshPublic,
		SSHRate:           sshRate,
		SSHBurst:          int(sshBurst),
	}, nil
}

func boolValue(parent map[string]any, path, key string) (bool, error) {
	value, ok := parent[key]
	if !ok {
		return false, fmt.Errorf("%s.%s: missing", path, key)
	}
	out, ok := value.(bool)
	if !ok {
		return false, fmt.Errorf("%s.%s: expected bool, got %T", path, key, value)
	}
	return out, nil
}

func intValue(value any, path string) (int, error) {
	switch v := value.(type) {
	case int:
		return v, nil
	case int64:
		return int(v), nil
	case float64:
		if v != float64(int64(v)) {
			return 0, fmt.Errorf("%s: expected integer, got %v", path, v)
		}
		return int(v), nil
	default:
		return 0, fmt.Errorf("%s: expected integer, got %T", path, value)
	}
}

func sortedStringSet(set map[string]bool) []string {
	out := make([]string, 0, len(set))
	for item := range set {
		out = append(out, item)
	}
	sort.Strings(out)
	if len(out) > 0 && out[0] != "lo" {
		for i, item := range out {
			if item == "lo" {
				copy(out[1:i+1], out[0:i])
				out[0] = "lo"
				break
			}
		}
	}
	return out
}

func sortedIntSet(set map[int]bool) []int {
	out := make([]int, 0, len(set))
	for item := range set {
		out = append(out, item)
	}
	sort.Ints(out)
	return out
}

func portSet(ports []int) string {
	if len(ports) == 1 {
		return fmt.Sprintf("%d", ports[0])
	}
	parts := make([]string, 0, len(ports))
	for _, port := range ports {
		parts = append(parts, fmt.Sprintf("%d", port))
	}
	return "{ " + strings.Join(parts, ", ") + " }"
}
